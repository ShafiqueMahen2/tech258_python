# Numeric Types --> Ints & Floats

# Ints - Whole numbers, can be positive or negative
a = 24
b = 16
print(type(a))

# Floats - Decimals
float_num = 1.356
int_num = 4

print(float_num + int_num)

# Floats are often rounded by python

one_third = 1 / 3
print(one_third)
# Should output 0.3333333333333333

print(one_third * 3)
# Will output rounded value of 1.0

