# Calculator program
# Addition Function
def addition(num1: int, num2: int) -> int:
    return num1 + num2


# Subtraction Function
def subtraction(num1: int, num2: int) -> int:
    return num1 - num2


# Multiplication Function
def multiplication(num1: int, num2: int) -> int:
    return num1 * num2


# Division Function
def division(num1: int, num2: int) -> float:
    return num1 / num2


# Function calls
print(addition(5, 5))
print(subtraction(5, 5))
print(multiplication(5, 5))
print(division(5, 5))
